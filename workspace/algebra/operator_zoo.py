def analyze_operator(M: np.ndarray, name: str) -> Dict:
    """Full analysis of a single operator."""
    eigs = np.linalg.eigvals(M)
    det = np.linalg.det(M)
    trace = np.trace(M)

    # Check if it's a projection (M² = M)
    is_projection = np.allclose(M @ M, M, atol=1e-10)

    # Check if it's involutory (M² = I)
    is_involution = np.allclose(M @ M, np.eye(len(M)), atol=1e-10)

    # Check if it's unitary (M†M = I for complex, M^T M = I for real)
    is_orthogonal = np.allclose(M.T @ M, np.eye(len(M)), atol=1e-10)

    # Check if column-stochastic (columns sum to 1, entries ≥ 0)
    is_stochastic = (np.all(M >= -1e-10) and
                     np.allclose(M.sum(axis=0), 1, atol=1e-10))

    # Order: smallest k such that M^k = I (or 0 if none, up to 12)
    order = 0
    power = np.eye(len(M))
    for k in range(1, 13):
        power = power @ M
        if np.allclose(power, np.eye(len(M)), atol=1e-10):
            order = k
            break

    return {
        'name': name,
        'matrix': M,
        'eigenvalues': eigs,
        'det': det,
        'trace': trace,
        'is_invertible': abs(det) > 1e-10,
        'is_projection': is_projection,
        'is_involution': is_involution,
        'is_orthogonal': is_orthogonal,
        'is_stochastic': is_stochastic,
        'order': order,  # 0 = infinite or >12
    }


def commutator_table(operators: Dict[str, np.ndarray]) -> np.ndarray:
    """Compute the commutation table: [A, B] = AB - BA.

    Returns matrix of commutator norms. Zero means they commute.
    """
    names = list(operators.keys())
    n = len(names)
    table = np.zeros((n, n))

    for i in range(n):
        for j in range(n):
            A = operators[names[i]]
            B = operators[names[j]]
            if A.shape == B.shape and np.isrealobj(A) and np.isrealobj(B):
                comm = A @ B - B @ A
                table[i, j] = np.linalg.norm(comm)
            else:
                table[i, j] = -1  # incompatible

    return table, names


def find_generated_group(generators: List[np.ndarray], max_size: int = 100) -> List[np.ndarray]:
    """Find the group generated by a list of matrices (closure under multiplication).

    Uses BFS: start with generators, multiply all pairs, add new elements,
    repeat until no new elements found.
    """
    def matrix_in_list(M, lst, tol=1e-10):
        for X in lst:
            if M.shape == X.shape and np.allclose(M, X, atol=tol):
                return True
        return False

    group = [np.eye(generators[0].shape[0])]  # Start with identity
    for g in generators:
        if not matrix_in_list(g, group):
            group.append(g.copy())

    changed = True
    while changed and len(group) < max_size:
        changed = False
        new_elements = []
        for A in group:
            for B in group:
                AB = A @ B
                if not matrix_in_list(AB, group) and not matrix_in_list(AB, new_elements):
                    new_elements.append(AB)
                    changed = True
        group.extend(new_elements)

    return group


