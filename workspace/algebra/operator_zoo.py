def build_zoo() -> Dict[str, Dict]:
    """Build and analyze the complete operator zoo."""

    # All real 3×3 operators from the seed
    operators = {
        'I':         op_identity(),
        'R':         op_z3_rotation(),
        'R²':        op_z3_rotation_inv(),
        'σ_TC':      op_tc_swap(),
        'σ_TB':      op_tb_swap(),
        'σ_CB':      op_cb_swap(),
        'Π_B':       op_boundary_project(),
        'Π_L':       op_live_project(),
        'Abs':       op_boundary_absorb(),
        'Mix':       op_uniform_mix(),
    }

    analyses = {}
    for name, M in operators.items():
        analyses[name] = analyze_operator(M, name)

    return operators, analyses


